SHOW INDEX FROM hr_order WHERE Key_name LIKE 'idx_hr_order%';
SHOW INDEX FROM manager_evaluation WHERE Key_name LIKE 'idx_mgr_eval%';
SHOW INDEX FROM employee WHERE Key_name LIKE 'idx_emp_company%';
