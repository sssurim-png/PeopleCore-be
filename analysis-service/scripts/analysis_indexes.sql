-- =====================================================================
-- 분석 서비스 — 추가 인덱스 (성능 최적화용)
-- ---------------------------------------------------------------------
-- 목적:
--   분석 도구가 자주 쓰는 컬럼에 인덱스 추가 → 쿼리 속도 ↑
--   hr-service 가 이미 만든 인덱스와 중복 가능 — IF NOT EXISTS 처리
--
-- 실행:
--   $ mysql -u root -ptest1234 peoplecore < analysis_indexes.sql
--   ※ 한 번만 실행 (CREATE INDEX 는 IF NOT EXISTS 미지원이라 ALTER TABLE ADD INDEX
--      실패해도 무시 가능 — 이미 존재하는 인덱스면 에러 메시지만 나오고 SKIP)
--
-- 영향:
--   - INSERT/UPDATE 약간 느려짐 (인덱스 유지 비용)
--   - SELECT 빠름 (분석용 쿼리)
--   - 운영 부담은 무시할 수준 (HR 데이터 양)
-- =====================================================================

USE peoplecore;


-- =====================================================================
-- 1. commute_record — #7 워라밸 / #9 산업안전 / #1 부당 보상
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE emp_id=? AND work_date BETWEEN ? AND ?
-- 이미 hr-service 가 만든 인덱스: idx_commute_company_emp_date, idx_commute_emp_date
-- → 충분 (추가 X)
-- =====================================================================


-- =====================================================================
-- 2. eval_grade — 모든 분석의 베이스
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE emp_id=? AND season_id IN (...)
-- =====================================================================
ALTER TABLE eval_grade
    ADD INDEX idx_eval_grade_season_emp (season_id, emp_id);


-- =====================================================================
-- 3. payroll_details — #1 / #2 (등급-연봉 분석)
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE emp_id=? AND payroll_run_id IN (...)
-- 이미 있음: idx_payroll_detail_run, idx_payroll_detail_emp
-- → 충분 (추가 X)
-- =====================================================================


-- =====================================================================
-- 4. payroll_runs — pay_year_month 기간 필터
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE pay_year_month BETWEEN ? AND ?
-- 이미 있음: idx_payroll_company_month
-- → 충분 (추가 X)
-- =====================================================================


-- =====================================================================
-- 5. hr_order — 진급 이력 조회 (#1 부당 보상)
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE emp_id=? AND order_type='PROMOTION' AND deleted_at IS NULL
-- =====================================================================
ALTER TABLE hr_order
    ADD INDEX idx_hr_order_emp_type_status (emp_id, order_type, status, deleted_at);


-- =====================================================================
-- 6. manager_evaluation — #5 평가자 분석
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE season_id=? AND evaluator_id=?
-- =====================================================================
ALTER TABLE manager_evaluation
    ADD INDEX idx_mgr_eval_season_evaluator (season_id, evaluator_id);


-- =====================================================================
-- 7. vacation_balance — #7 워라밸 (연차 사용률)
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE emp_id=? AND balance_year=?
-- 이미 있음: idx_vacation_balance_company_emp
-- → grade ⌒ 충분, 추가 X
-- =====================================================================


-- =====================================================================
-- 8. employee — 분석 대상 사원 필터
-- ---------------------------------------------------------------------
-- 자주 쓰는 패턴: WHERE company_id=? AND emp_status='ACTIVE' AND emp_resign IS NULL
-- =====================================================================
ALTER TABLE employee
    ADD INDEX idx_emp_company_status_resign (company_id, emp_status, emp_resign);


